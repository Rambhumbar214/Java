public class Shortrange{
public static void main(String[] args)
{
System.out.println("SHORT:");
System.out.println("Range: -32,768 to 32,767");
System.out.println("One less than min (cast): " + (short)(-32_769));
System.out.println("One more than max (cast): " + (short)(32_768));
System.out.println();
}
}