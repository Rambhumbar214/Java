public class Floatrange{
public static void main(String[] args)
{
System.out.println("FLOAT:");
System.out.println("Smallest positive: " + Float.MIN_VALUE);
System.out.println("Largest value: " + Float.MAX_VALUE);
System.out.println("Even smaller (divided): " + (Float.MIN_VALUE / 10));
System.out.println("Way too big (multiplied): " + (Float.MAX_VALUE * 10));
System.out.println();
}
}